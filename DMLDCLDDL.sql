-- Data manipulation language (DML)
    -- SELECT
    -- INSERT
    -- UPDATE
    -- DELETE
    -- MERGE

-- Data definition language (DDL)
    -- CREATE
    -- ALTER
    -- DROP
    -- RENAME
    -- TRUNCATE
    -- COMMENT

-- Data control language (DCL)
    -- GRANT 
    -- REVOKE

-- Transaction control 
    -- SAVEPOINT
    -- COMMIT
    -- ROLLBACK

-- DDL (Data Definition Language) commands are used to define the database structure or schema.
-- DQL (Data Query Language) commands are used to retrieve data from the database. 
-- DML (Data Modification Language) commands are used to modify data in the database. 
-- DCL (Data Control Language) commands are used to control access to the database. 
-- TCL (Transaction Control Language) commands are used to manage the transactions in the database. 